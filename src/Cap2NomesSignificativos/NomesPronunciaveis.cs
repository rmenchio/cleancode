/*
 * A ideia principal é escolher nomes que sejam pronunciáveis.
 * 
 * Escrever nomes que sejam pronunciáveis ajudam na comunicação.
 */

namespace CleanCode.Cap2NomesSignificativos
{
	public class NomesPronunciaveis
	{
		void Exemplo1()
		{
			void CrAMD() { }

            void CriarAnoMesDia() { }

			// Fica claro neste exemplo que para um programador se
			// comunicar com o outro, pronunciando "CrAMD" seria algo bizarro,
			// e até engraçado, mas não facilitaria a comunicação e nem o
			// entendimento de um programador novo no projeto.

			// Busque sempre por nomes pronunciáveis.
        }
	}
}